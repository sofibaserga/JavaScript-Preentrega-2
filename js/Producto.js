class Producto {
    nombre;
    bodega;
    varietal;
    precio;
    subcategoria;
    
    constructor(nombre,bodega,varietal,precio,subcategoria) {
        this.nombre = nombre;
        this.bodega = bodega;
        this.varietal = varietal;
        this.precio= precio;
        this.subcategoria = subcategoria;
    }
}